#include "libft.h"
#include <unistd.h>

void	ft_new_line(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] == ' ')
			write(1, "\n", 1);
		write(1, &str[i], 1);
		i++;
	}
	write(1, "\n", 1);
}
